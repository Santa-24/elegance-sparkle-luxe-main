select
  b.booking_date,
  count(*) as booking_count,
  coalesce(sum(b.amount_quoted), 0) as quoted_amount,
  coalesce(sum(b.amount_paid), 0) as received_amount,
  coalesce(sum(coalesce(b.amount_quoted, 0) - coalesce(b.amount_paid, 0)), 0) as pending_amount,
