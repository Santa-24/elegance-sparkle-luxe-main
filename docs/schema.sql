create or replace view public.upcoming_bookings as
select
  b.id,
  b.booking_code,
  b.customer_name,
  b.phone,
  b.email,
  b.service_name,
  b.booking_date,
  b.booking_time,
  b.duration_minutes,
  b.status,
  b.source,
  cp.preferred_contact_method,
  s.duration_label,
  s.category
