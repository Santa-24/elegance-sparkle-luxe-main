create or replace function public.generate_booking_code()
returns varchar
language plpgsql
as $$
declare
  new_code varchar;
  attempt_count int := 0;
begin
  loop
    new_code := 'EM-' || to_char(current_date, 'YYMMDD') || '-' || lpad((floor(random() * 9000) + 1000)::text, 4, '0');
    if not exists (select 1 from public.bookings where booking_code = new_code) then
      return new_code;
    end if;

    attempt_count := attempt_count + 1;
    if attempt_count > 1000 then
      raise exception 'Could not generate unique booking code';
    end if;
  end loop;
end;
$$;

create or replace function public.update_customer_preferences()
returns trigger
language plpgsql
as $$
begin
  if new.deleted_at is null and new.status = 'completed' then
    if tg_op = 'INSERT' or old.status is distinct from new.status then
      insert into public.customer_preferences (
        customer_phone,
        customer_email,
        customer_name,
        total_bookings,
        total_spent,
        last_booking_date
      ) values (
        new.phone,
        new.email,
        new.customer_name,
        1,
        coalesce(new.amount_paid, 0),
        new.booking_date
      )
      on conflict (customer_phone) do update set
        customer_email = coalesce(excluded.customer_email, public.customer_preferences.customer_email),
        customer_name = excluded.customer_name,
        total_bookings = public.customer_preferences.total_bookings + 1,
        total_spent = public.customer_preferences.total_spent + coalesce(excluded.total_spent, 0),
        last_booking_date = excluded.last_booking_date,
        updated_at = now();
    end if;
  end if;

  return new;
end;
$$;

drop trigger if exists trigger_update_customer_preferences on public.bookings;
create trigger trigger_update_customer_preferences
after insert or update of status on public.bookings
for each row
execute function public.update_customer_preferences();

create or replace function public.trigger_notifications_on_booking_update()
returns trigger
language plpgsql
as $$
begin
  if tg_op = 'UPDATE' and old.status is distinct from new.status then
    insert into public.notifications (
      booking_id,
      customer_email,
      customer_phone,
      notification_type,
      template_name,
      channel,
      status
    ) values (
      new.id,
      new.email,
      new.phone,
      'status_update',
      'booking_status_changed',
      'email',
      'pending'
    );
  end if;

  return new;
end;
$$;

