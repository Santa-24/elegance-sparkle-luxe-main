  id bigint generated by default as identity primary key,
  user_id uuid,
  action varchar(50) not null,
  resource_type varchar(80) not null,
  resource_id text not null default '',
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

alter table public.audit_logs
  add column if not exists created_at timestamptz default now();
alter table public.audit_logs
  drop constraint if exists audit_logs_user_id_fkey;
alter table public.audit_logs
  add column if not exists resource_type varchar(80);
alter table public.audit_logs
  add column if not exists resource_id text default '';
alter table public.audit_logs
  add column if not exists metadata jsonb default '{}'::jsonb;
alter table public.audit_logs
  alter column metadata set default '{}'::jsonb;
alter table public.audit_logs
  alter column resource_id set default '';
alter table public.audit_logs
  alter column resource_type set default 'unknown';

do $$
begin
  if exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'audit_logs'
      and column_name = 'timestamp'
  ) then
    execute $sql$
      update public.audit_logs
      set created_at = coalesce(created_at, timestamp)
      where created_at is null
    $sql$;
  end if;

  if exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'audit_logs'
      and column_name = 'table_name'
  ) then
    execute $sql$
      update public.audit_logs
      set resource_type = coalesce(resource_type, table_name, 'unknown')
      where resource_type is null
    $sql$;
  end if;

  if exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'audit_logs'
      and column_name = 'record_id'
  ) then
    execute $sql$
      update public.audit_logs
      set resource_id = coalesce(resource_id, record_id, '')
      where resource_id is null
    $sql$;
  end if;

  if exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'audit_logs'
      and column_name = 'old_values'
  ) then
    execute $sql$
      update public.audit_logs
      set metadata = coalesce(metadata, jsonb_build_object('old_values', old_values, 'new_values', new_values))
      where metadata is null
    $sql$;
  end if;
end;
$$;

